"""Splitting a dictionary into two lists!"""

__author__: str = "730520183"


def get_keys(dictionary: dict[str, int]) -> list[str]:
    """Put keys in order in a string."""
    return list(dictionary.keys())


def get_values(test: dict[str, int]) -> list[int]:
    """Put all values in order in a string."""
    return list(test.values())
